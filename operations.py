def add(num_1, num_2):
    result = int(num_1) + int(num_2)
    print(f'{num_1} + {num_2} is equal to {result}')
    return result
    
def rest(num_1, num_2):
    result = int(num_1) - int(num_2)
    print(f'{num_1} - {num_2} is equal to {result}')
    return result

def mult(num_1, num_2):
    result = int(num_1) * int(num_2)
    print(f'{num_1} * {num_2} is equal to {result}')
    return result

def div(num_1, num_2):
    result = int(num_1) / int(num_2)
    print(f'{num_1} / {num_2} is equal to {result}')
    return result

def pot(num_1, num_2):
    result = int(num_1) ** int(num_2)
    print(f'{num_1} ** {num_2} is equal to {result}')
    return result

def mod(num_1, num_2):
    result = int(num_1) % int(num_2)
    print(f'{num_1} ** {num_2} is equal to {result}')
    return result
